/**Write a program that takes two numbers and displays 
 * their sum, difference, product, and quotient */
const Num1 = Number(25);
const Num2 = Number(35);

const sum = Num1 + Num2;
const diff = Num1 - Num2;
const product = Num1 * Num2;
const quotient = Num1 / Num2;

console.log(` Sum: ${sum} \n Difference: ${diff} \n Product: ${product} \n Quotient: ${quotient}`);